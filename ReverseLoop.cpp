//  ------------------------------
//        REVERSE FOR LOOP FROM N TO 1
//  ------------------------------
//  Before jumping into the code, please try it yourself first :)

#include <iostream>
using namespace std;
int main()
{
    int n;

    cout << "Entern n : ";

    cin >> n;

    for (int i = n; i > 0; i -= 1)
    {
        cout << i << " ";
    }
    return 0;
}


